# ╔═╡ d4501482-af50-4bee-9cb6-8595fe898b7b
@model function normal1d(x)

    # define parameters through a prior
    μ ~ Normal(0, 10) # very weak
    σ ~ TruncatedNormal(0, 2.5, 0, Inf) # trunace so cannot be >1

    # Data model
    return x .~ Normal(μ, σ)
end

# ╔═╡ a71badff-2aa1-421a-8280-a0967c9b39be
heights_posterior = let
    model = normal1d(heights)
    sampler = DynamicNUTS()
    n_per_chain = 5000
    n_chains = 4
    sample(model, sampler, MCMCThreads(), n_per_chain, n_chains; drop_warmup=true)
end

# ╔═╡ 76c0d8fa-73e1-4ee7-a818-851cb564faae
let
    μpost = vec(heights_posterior[:μ])
    σpost = vec(heights_posterior[:σ])
    N = length(heights)
    scatter(
        μpost,
        σpost;
        title="How are heights of 7th grade boys distributed?\nN=$N",
        markersize=0.125,
        color=:black,
        alpha=0.5,
        label=false,
        xlabel=L"$\mu$ [ft]",
        ylabel=L"$\sigma$ [ft]",
    )
end

# ╔═╡ ef3db979-627c-4b02-8777-521ca8b09a6b
md"""
When you're looking at this scatter plot, you should imagine each dot as a distribution.
That is, each dot is telling you that you sampled a distribution with that mean and that standard deviation.
Here's another way of visualizing that "sample of distributions":
"""

# ╔═╡ 222b41c5-cd87-4630-bbda-08536cbe1319
let
    μpost = vec(heights_posterior[:μ])
    σpost = vec(heights_posterior[:σ])
    normal_dists = [Normal(μ, σ) for (μ, σ) in zip(μpost, σpost)]
    p = plot(; xlabel=L"$y$", ylabel="Posterior Predictive Density")
    for i in 1:500
        plot!(p, normal_dists[i]; label=false, color=:gray, alpha=0.1)
    end
    p
end

# ╔═╡ 645bc843-7a32-4a0d-bc95-74f08d5b484a
md"""
This is called the "posterior predictive density" because it's telling us about the distribution **of $y$** (i.e., height).
We would expect new data on heights to follow this predictive distribution.

Another way of interpreting this is as a full characterization of the uncertainty in our understanding **of $y$** given the available data.
Plotting things this way is often valuable because typically we are doing this model to learn about $y$, not about $\mu$ and $\sigma$.
For example, if we collected some data on extreme rainfall and built a model for hazard, the end use for our analysis might be an estimate of future rainfall hazard.
"""

