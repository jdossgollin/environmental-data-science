# ╔═╡ a5c07f65-bf90-43c6-b109-3ea2e9717d7c
md"""
# Posterior estimation with Monte Carlo sampling

In the notebook on Maximum Likelihood Estimation, we saw ways to define models using Turing, and then to calculate their maximum likelihood estimates.
In this notebook, we'll explore techniques that bring us near the state of the art for model estimation.

Bayesian computation is an incredibly complex field, and worth its own course.
There's lots to think about!

Thus far we have seen models for $p(\theta | y)$ that are simple enough that we can solve them on a grid.
Grid-based methods of inference work well for simple models, aren't used much in the real world.
One reason is that most of the *probability mass* is clustered in a very small region of what the grid might look like.
If this feels a bit vague, go read Michael Betancourt's essay [Probabilistic Computation](https://betanalpha.github.io/assets/case_studies/probabilistic_computation.html).

What you need to know for now is that, through some cool math, it is possible (under controlled circumstances!) to draw samples from $p(\theta | y)$ through methods known as "Monte Carlo" methods (we won't get into the details here -- go take a Bayesian computation class!)

The key thing to know about these samples is that **they allow us to estimate expectations about our posterior.**
Most obviously, if we draw $N$ samples, $\hat\theta_1, \ldots, \hat\theta_N$ then
```math
\mathbb{E}[\theta] \approx \frac{1}{N} \sum_{i=1}^N \theta_i
```

## Model definition

Let's see an example -- and what better than our beloved coin flipping experiment!
"""

# ╔═╡ 5b9986fa-5c4a-4ade-9494-01fcc099ea96
begin
    coin_flips = ["H", "H", "H", "T", "H", "H", "H", "H", "H"]
    heads = [flip == "H" for flip in coin_flips]
    N = length(coin_flips)
    n_heads = sum(heads)
end;

# ╔═╡ 27ebfe8a-8296-44b9-a2f0-cb73452b13f0
md"We can define stuff outside the model"

# ╔═╡ cad7ba7b-14f0-4f53-af73-af29fd3d6008
prior_dist = Beta(5, 5)

# ╔═╡ 5d8b0727-08dd-4b2e-a783-1462ec68ccac
md"and use it inside"

# ╔═╡ 82610f52-cd2a-46f9-89c0-7edfd982a455
@model function coinflip(x)

    # now our priors do matter!
    p ~ prior_dist

    # The number of observations.
    return x .~ Bernoulli(p)
end

# ╔═╡ c90367b6-ede8-45b5-b136-07626ffcbcc1
md"""
## Sampling

As mentioned earlier, our goal is to draw random samples from $p(\theta | y)$.
Here's the syntax to do that
"""

# ╔═╡ 6e334d4a-3708-492d-9c1f-ccb7312adc2f
chain = let
    model = coinflip(heads)
    sampler = DynamicNUTS()
    nsamples = 5000
    sample(model, sampler, nsamples; drop_warmup=true)
end

# ╔═╡ 676d449d-0fdb-4182-92aa-829e08c85868
md"""
## Some simple checks

Now that we have our chains, we want to run some checks!

Our posterior actually has a closed form solution (look for Beta-Binomial models if you want to read more) that we can use to run some checks!
"""

# ╔═╡ cdc1df59-6e4b-46a7-beaa-99f8ca52f005
closed_form = Beta(prior_dist.α + n_heads, prior_dist.β + N - n_heads)

# ╔═╡ 70d34869-42c4-4df3-b7ef-82e94c8354f5
md"which we will use to check our answers"

# ╔═╡ f5e16807-a578-4ee0-bee3-c73574672ee8
"Sample" => mean(chain[:p]), "True" => mean(closed_form)

# ╔═╡ 9e1c3c19-d8ae-4ac2-be21-159345d69fb1
md"""
We did good!
We can also look at plots (normally we don't have a closed form solution!)
One of the things to look for is whether the chain looks like it is approximately IID -- that's what we need!
"""

# ╔═╡ 4c7ab668-4b4d-456f-adb9-98eb5133d75f
plot(chain)

# ╔═╡ a63b7434-6948-4c34-bfb0-430248e874b0
md"""
A histogram shows that our samples very closely match the true posterior
"""

# ╔═╡ 768684e1-e43f-4fce-b482-2c5392e0ba7b
let
    p_summary = chain[:p]
    histogram(p_summary; label="Posterior", normalize=true, legend=:topleft)
    closed_form = Beta(5 + 8, 5 + 1)
    plot!(closed_form; label="Closed Form", linewidth=3)
end

# ╔═╡ 46baa45f-fa28-47a3-9c5c-d719a96c004f
md"""
## Multiple chains

Typically we don't want to run a single chain (the common term for the Monte Carlo samples).
Instead, we often want to run several.
Here's how to do that:
"""

# ╔═╡ d9703f72-776c-421d-9f0d-f40e1a0a8b5c
chains = let
    model = coinflip(heads)
    sampler = DynamicNUTS()
    n_per_chain = 5000
    nchains = 4
    sample(model, sampler, MCMCThreads(), n_per_chain, nchains; drop_warmup=true)
end

# ╔═╡ ed2184e5-5774-4bc4-8e00-acd3ee3c294e
md"""
One thing this lets us check is whether the chains we ran have converged and are well mixed
"""

# ╔═╡ c454f554-dc42-432f-a3f2-69692115bd75
plot(chains)

# ╔═╡ 0284a998-8d75-4119-97c8-0a6434e4d16b
md"We can again plot the posterior of our chains"

# ╔═╡ 333d29bd-bd83-487e-ac8f-0177288d6909
let
    p_summary = vec(chains[:p])
    histogram(p_summary; label="Posterior", normalize=true, legend=:topleft)
    plot!(closed_form; label="Closed Form", linewidth=3)
end

